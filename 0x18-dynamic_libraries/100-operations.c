#include <stdio.h>
#include "main.h"
/**
* add - function to add 2 numbers
* @a: the first operand
* @b: the second operand
* Return: the resultant operation
*/

int add(int a, int b)
{
    return(a + b);
}

 /**
 * sub - function to subtract 2 numbers
 * @a: the first operand
 * @b: the second operand
 * Return: the resultant operation
 */
 int sub(int a, int b)
 {
     return(a - b);
 }

 /**
 * mul - function to multiply 2 numbers
 * @a: the first operand
 * @b: the second operand
 * Return: the resultant operation
 */
 int mul(int a, int b)
 {
     return(a * b);
 }


 /**
 * div - function to divide 2 numbers
 * @a: the first operand
 * @b: the second operand
 * Return: the resultant operation
 */
 int div(int a, int b)
 {
     return(a / b);
 }


 /**
 * mod - function to get modulus
 * @a: the first operand
 * @b: the second operand
 * Return: the resultant operation
 */
 int mod(int a, int b)
 {
     return(a % b);
 }
